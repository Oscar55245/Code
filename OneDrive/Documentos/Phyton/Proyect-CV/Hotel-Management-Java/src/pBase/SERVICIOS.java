public void salir(){
if ((txtTotal.getText().equals(""))) {SH=0;}else{SH+=Integer.parseInt(txtTotal.getText());}
        if ((txtTotal1.getText().equals(""))){E=0;}else{E+=Integer.parseInt(txtTotal1.getText());}
        if ((txtTotal2.getText().equals(""))) {EXTR=0;}else{EXTR+=Integer.parseInt(txtTotal2.getText());} 
        System.out.println(JDRESER.COMPLETO);
        JDRESER.COMPLETO+=SH+E+EXTR;
        System.out.println("-----"+SH);
        System.out.println(E);
        System.out.println(EXTR);
        JDRESER.txtCosto.setText(String.valueOf(JDRESER.COMPLETO));
        System.out.println("Costo"+JDRESER.COMPLETO);
        txtTotal.setText("");
        txtTotal1.setText("");
        txtTotal2.setText("");
        total=0;
        SH=0;
        E=0;
        EXTR=0;
        this.hide();  

}
